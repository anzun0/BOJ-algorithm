#include <bits/stdc++.h>
using namespace std;
int n, m;
int a[500][500];
int t[19][3][2] = { // 총 19개의 경우의 수
    {{0,1}, {0,2}, {0,3}},
    {{1,0}, {2,0}, {3,0}},
    {{1,0}, {1,1}, {1,2}},
    {{0,1}, {1,0}, {2,0}},
    {{0,1}, {0,2}, {1,2}},
    {{1,0}, {2,0}, {2,-1}},
    {{0,1}, {0,2}, {-1,2}},
    {{1,0}, {2,0}, {2,1}},
    {{0,1}, {0,2}, {1,0}},
    {{0,1}, {1,1}, {2,1}},
    {{0,1}, {1,0}, {1,1}},
    {{0,1}, {-1,1}, {-1,2}},
    {{1,0}, {1,1}, {2,1}},
    {{0,1}, {1,1}, {1,2}},
    {{1,0}, {1,-1}, {2,-1}},
    {{0,1}, {0,2}, {-1,1}},
    {{0,1}, {0,2}, {1,1}},
    {{1,0}, {2,0}, {1,1}},
    {{1,0}, {2,0}, {1,-1}},
};

int main(void) {
    ios_base::sync_with_stdio(false);
    cin.tie(0); cout.tie(0);
    
    cin >> n >> m;
    for (int i = 0; i < n; i++)
        for (int j = 0; j < m; j++)
            cin >> a[i][j];
    
    int ans = 0;
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < m; j++) {
            
            for (int x = 0; x < 19; x++) {
                int temp = a[i][j];
                
                for (int y = 0; y < 3; y++) {
                    int nx = i + t[x][y][0];
                    int ny = j + t[x][y][1];
                    if (nx < 0 || nx >= n || ny < 0 || ny >= m) {
                        temp = -1;
                        break;
                    }
                    temp += a[nx][ny];
                }
                ans = max(ans, temp);
            }
        }
    }
    cout << ans << '\n';
}
